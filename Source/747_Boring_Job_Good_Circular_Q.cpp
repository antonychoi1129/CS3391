#include <stdio.h>

struct MagicNumber {
	int value;
	int index;
};

int N;  //N - Number of magic numbers
MagicNumber S[1000];    //The sequence	*Circular_Q, better approach than assigning memory blindly.
int head, tail;

void Spush(MagicNumber num){
	S[tail] = num;
	S[tail].value -= 1;
	tail = (tail + 1) % N;
}


int main(){
	int T;
	int K;      //K - Magic parameter
	int maxPos;
	int i;

	scanf("%i", &T);
	for (int cases = 0; cases < T; cases++) {
		scanf("%i %i", &N, &K);

		//Init & get number
		head = tail = 0;
		for (tail = 0; tail < N; tail++) {
			scanf("%i", &S[tail].value);
			S[tail].index = tail;
		}

		for (int itr = N; itr > 0; itr--) {
			if (K > itr) K = itr;

			//Find Vmax
			maxPos = head;
			for (i = 1; i < K; i++)
				if (S[(head + i) % N].value > S[maxPos].value)
					maxPos = (head + i) % N;

			//Print the result beforehand
			if (N != itr) printf(" ");
			printf("%i", S[maxPos].index + 1);

			//-1 and append
			tail = (head + itr) % N;
			for (i = 0; i < K; i++) {
				if ((i + head) % N == maxPos) continue;
				Spush(S[(head + i) % N]);
			}

			head = (head + K) % N;
		}
		printf("\n");
	}
	return 0;
}
