#include <stdio.h>

struct magicNumber{
	int value;
	int index;
};

int main(){
	//BAD EXAMPLE
	int T;
	int N, K;      			//	N - Number of magic numbers, K - Magic parameter
	magicNumber S[19890];	//The sequence
	int head, tail;
	int maxPos;
	int i;

	scanf("%i",&T);
	for (i = 0; i < T; i++) {
		scanf("%i %i",&N, &K);

		head = tail = 0;
		for (tail = 0; tail < N; tail++) {
			scanf("%i",&S[tail].value);
			S[tail].index = tail;
		}

		while (N > 0) {
			if (K > N) K = N;

			//Find Vmax
			maxPos = head;
			for (int i = head + 1; i < K + head; i++)
				if (S[i].value > S[maxPos].value) maxPos = i;

			//-1 and append
			for (int i = head; i < K + head; i++) {
				if (i == maxPos) continue;
				S[tail].value = S[i].value - 1;
				S[tail].index = S[i].index;
				tail++;
			}

			if (head != 0) printf(" ");
			printf("%i",S[maxPos].index + 1);

			head += K;
			N--;
		}
		printf("\n");
	}


	return 0;
}

